/*
C언어란?
C언어는 프로그래밍 언어이다.
사람이 컴퓨터가 인식하는 기계어를 공부하는것은 부담이 크고
컴퓨터가 사람의 언어를 가르치는 것도 쉽지 않다.
그래서 우리는 C라는 언어를 배우고 '컴파일러'를 사용하게 된다.

컴파일러?
컴파일러는 C언어와 기계어를 이해하는 프로그램이다.
사람은 C언어만 할줄 알고 컴퓨터는 기계어를 알기 때문에 컴파일러를 이용해
C언어를 기계어로 번역해 컴퓨터에 전달해준다.
이렇게 번역하는 일 자체를 가리켜 '컴파일(compile)'이라고 한다.

프로그래밍 언어란?
사람과 컴파일러가 이해할 수 있는 약속된 형태의 언어를 의미한다. 
C언어도 프로그래밍 언어중 하나이다.

 
*/